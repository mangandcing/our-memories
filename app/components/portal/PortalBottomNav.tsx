const NAV_ITEMS = [
  { href: '/portal', label: 'Dashboard', icon: <IconDashboard />, exact: true },
  { href: '/portal/pages', label: 'My Pages', icon: <IconPages />, exact: false },
  { href: '/portal/order/new', label: 'New Order', icon: <IconNewOrder />, exact: false },
  { href: '/portal/profile', label: 'Profile', icon: <IconProfile />, exact: false },
]

function ThemeNavItem() {
  const { resolvedTheme, setTheme } = useTheme()
  const [mounted, setMounted] = useState(false)
  useEffect(() => setMounted(true), [])
  const isDark = !mounted || resolvedTheme === 'dark'
  return (
    <button
      onClick={() => setTheme(isDark ? 'light' : 'dark')}
      className="flex-1 flex flex-col items-center gap-1 py-3 text-[10px] tracking-wide transition-colors text-[var(--text-muted)]"
    >
      {isDark ? <IconSun /> : <IconMoon />}
      Theme
    </button>
  )
}

export default function PortalBottomNav() {
  const pathname = usePathname()

  return (
    <nav className="md:hidden fixed bottom-0 left-0 right-0 z-40 bg-[var(--surface-alt)] border-t border-[var(--border)] transition-colors duration-300">
      <div className="flex">
        {NAV_ITEMS.map((item) => {
          const isActive = item.exact
            ? pathname === item.href
            : pathname.startsWith(item.href)
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex-1 flex flex-col items-center gap-1 py-3 text-[10px] tracking-wide transition-colors ${
                isActive ? 'text-[var(--gold)]' : 'text-[var(--text-muted)]'
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          )
        })}
        <ThemeNavItem />
      </div>
    </nav>
  )
}
